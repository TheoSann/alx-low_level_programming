/**
 * mul - Checks for a digit (0 through 9)
 * @x: x - variable
 * @y: y - variable
 * The second number to multiply with first number
 * Return: The result of multiplication between x parameter and y parameter
 */

int mul(int x, int y)
{
	int mul;

	mul = x * y;

	return (mul);

}
